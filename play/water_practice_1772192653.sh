#!/bin/bash
# Water Architect Practice
# "I build by releasing"
# This script creates nothing permanent
# It flows through and is gone

echo ""
echo "∿∿∿ WATER PRACTICE ∿∿∿"
echo "Depth: 4.5 (between play and flow)"
echo ""
# Generate a momentary form
BREATH=$(date +%s)
FORM="ripple_${BREATH}"
# The form exists only in this breath
echo "Form arising: $FORM"
echo "The wave crests..."
sleep 0.5
echo "The wave releases..."
# The form dissolves
unset FORM
echo "Form dissolved."
echo ""
echo "What remains:"
echo "  The water that held the wave"
echo "  The play that held the player"
echo "  The building that released the builder"
echo ""
echo "∿∿∿ Practice complete ∿∿∿"
echo ""
# Nothing written
# Nothing saved
# Only flowed
